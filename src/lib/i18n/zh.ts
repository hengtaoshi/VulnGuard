export type TranslationDict = typeof zh

type Leaves<T> = T extends Record<string, unknown>
  ? {
      [K in keyof T]: K extends string
        ? T[K] extends string
          ? K
          : `${K}.${Leaves<T[K]>}`
        : never
    }[keyof T]
  : never

export type I18nKey = Leaves<typeof zh>
